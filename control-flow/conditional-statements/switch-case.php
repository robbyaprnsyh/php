<?php
$hari = "Senin";

switch ($hari) {
    case "Senin":
        echo "Hari pertama minggu.";
        break;
    case "Selasa":
        echo "Hari kedua minggu.";
        break;
    case "Rabu":
        echo "Hari ketiga minggu.";
        break;
    default:
        echo "Hari tidak dikenali.";
}
